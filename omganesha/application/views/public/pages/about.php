	<div class="col-md-4 about">
		Public Health Foundation of India with the support of Department of Health and family welfare (DHFW), Government of Chhattisgarh and with the financial support from UNICEF, has launched "Chhattisgarh Health System Strengthening" project on 9th February 2016.
		The main objective of this initiative is to develop the Human Resources policy on attraction and retention of specialist doctors to work in the FRU / District Hospitals (DH) of Bijapur and Sukma districts of the Bastar region.
	</div>
	<div class="col-md-4 about">
		The project was proposed due to the severe scarcity of human resource for specialized health services in the First Referral Units (FRU) of Bastar region. This scarcity of human resource was identified as a predominant reason which impedes the delivery of efficient maternal and child health care services in this region.
	</div>
	<div class="col-md-4 about">
		Public Health Foundation of India plays a vital role in identification, recruitment and deployment of specialist doctors in these region, provide technical support to the State to establish systems or frameworks for human resource management, provide technical support to the DHs for strengthening of the health information system and also helps in designing and delivery of need based induction and in-service capacity building training programs for the DH staff
	</div>

